function handleErrorResponse(err, errTitle, instance) {
    console.log(err)
    var response = err.responseJSON
    var alertText = ''
    if (response.message) {
        alertText = response.message
    } else {
        alertText = generateErrorMessage(response)
    }

    instance.$alert(alertText, errTitle, {
        confirmButtonText: 'OK',
        dangerouslyUseHTMLString: true
    });
}

function generateErrorMessage(errArray) {
    var message = '<ul>'
    for (var i = 0; i < errArray.length; i++) {
        if (errArray[i].errorMessage.includes('<') || errArray[i].errorMessage.includes('>')) continue
        message += '<li>' + errArray[i].errorMessage + '</li>'
    }
    message += '</ul>'
    return message
}

